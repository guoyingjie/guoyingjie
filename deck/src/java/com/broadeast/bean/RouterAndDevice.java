package com.broadeast.bean;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Id;
import org.nutz.dao.entity.annotation.Table;

@Table("v1_router_device")
public class RouterAndDevice {
	@Id
	private int id;
	@Column("portal_user_id")
	private int userId;
	@Column("device_mac")
	private String devicemac;
	@Column("token")
	private String token;
	@Column("oldAuthUrl")
	private String oldAuthUrl;
	@Column("user_ip")
	private String userIp;
	@Column("siteId")
	private int siteId;
	@Column("basip")
	private String basip;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getDevicemac() {
		return devicemac;
	}
	public void setDevicemac(String devicemac) {
		this.devicemac = devicemac;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	public String getOldAuthUrl() {
		return oldAuthUrl;
	}
	public void setOldAuthUrl(String oldAuthUrl) {
		this.oldAuthUrl = oldAuthUrl;
	}
	
	public String getUserIp() {
		return userIp;
	}
	public void setUserIp(String userIp) {
		this.userIp = userIp;
	}
	
	public int getSiteId() {
		return siteId;
	}
	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}
	
	public String getBasip() {
		return basip;
	}
	public void setBasip(String basip) {
		this.basip = basip;
	}
	@Override
	public String toString() {
		return "RouterAndDevice [id=" + id + ", userId=" + userId
				+ ", devicemac=" + devicemac + ", token=" + token
				+ ", oldAuthUrl=" + oldAuthUrl + ", userIp=" + userIp
				+ ", siteId=" + siteId + ", basip=" + basip + "]";
	}
	


	
	
}
